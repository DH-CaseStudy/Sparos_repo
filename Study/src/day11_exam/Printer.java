package day11_exam;

public class Printer {

    public void println(int print){
        System.out.println(print);
    }

    public void println(boolean print){
        System.out.println(print);
    }

    public void println(double print){
        System.out.println(print);
    }

    public void println(String print){
        System.out.println(print);
    }
}
